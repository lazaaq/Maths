a: int = int(input("berapa detik? :"))
b = int(a/2592000)
c = int(a/86400-b*30)
d = int(a/3600-b*720-c*24)
e = int(a/60-b*43200-c*1440-d*60)
f = int(a-b*2592000-c*86400-d*3600-e*60)
print(a, "detik sama dengan :")
print(b, "bulan")
print(c, "hari")
print(d, "jam")
print(e, "menit")
print(f, "detik")